using System;
using System.Collections.Generic;
using System.Text.RegularExpressions;
using UnityEngine;

public class CSVLoader
{
	private TextAsset csvFile;

	private char lineSeperator = '\n';

	private char surround = '"';

	private string[] fieldSeperator = new string[1] { "," };

	public void LoadCSV()
	{
		csvFile = Resources.Load<TextAsset>("localisation");
	}

	public Dictionary<string, string> GetDictionaryValues(string attributeID)
	{
		Dictionary<string, string> dictionary = new Dictionary<string, string>();
		string[] array = csvFile.text.Split(lineSeperator);
		int num = -1;
		string[] array2 = array[0].Split(fieldSeperator, StringSplitOptions.None);
		for (int i = 0; i < array2.Length; i++)
		{
			if (array2[i].Contains(attributeID))
			{
				num = i;
				break;
			}
		}
		if (num == -1)
		{
			Debug.LogError("Could not find language column: " + attributeID);
			return dictionary;
		}
		Regex regex = new Regex(",(?=(?:[^\"]*\"[^\"]*\")*(?![^\"]*\"))");
		int num2 = 0;
		foreach (string input in array)
		{
			string[] array3 = regex.Split(input);
			for (int k = 0; k < array3.Length; k++)
			{
				string text = array3[k].Trim();
				if (text.StartsWith("\"") && text.EndsWith("\""))
				{
					text = text.Substring(1, text.Length - 2);
				}
				array3[k] = text;
			}
			if (array3.Length > num)
			{
				string key = array3[0];
				if (!dictionary.ContainsKey(key))
				{
					string value = array3[num];
					dictionary.Add(key, value);
				}
			}
			else
			{
				num2++;
			}
		}
		if (num2 > 0)
		{
			Debug.LogWarning($"{num2} row(s) had fewer fields than expected. Headers count: {array2.Length}");
		}
		return dictionary;
	}
}
